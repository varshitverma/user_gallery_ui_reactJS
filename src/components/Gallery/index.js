class Gallery extends Component {
  state = {activeImage: imagesList[0]}

  fetchSelectImg = imgDetails => {
    const {imageUrl, imageAltText} = imgDetails
    return <img src={imageUrl} className="main-image" alt={imageAltText} />
  }

  onChangeImg = selected => {
    this.setState({activeImage: selected})
  }

  render() {
    const {activeImage} = this.state

    return (
      <div className="bg-container">
        <div className="card">
          {this.fetchSelectImg(activeImage)}
          <h1 className="heading">Nature Photography</h1>
          <p className="desc">Nature Photography by User</p>
          <ul className="img-container">
            {imagesList.map(eachImg => (
              <ThumbnailItem
                key={eachImg.id}
                image={eachImg}
                onChangeImg={this.onChangeImg}
                isActive={activeImage.id === eachImg.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
